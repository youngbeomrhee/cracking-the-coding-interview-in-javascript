import { countDifferentBits } from '../../src/ch009-005-bit/ex506_countDifferentBits'

describe('', () => {
    const data = [
        [['11101', '01111'], 2],
        [['11101', '00111'], 3],
        [['101010', '111111'], 3],
        [['00001', '00010'], 2],
        [['1100', '1010'], 2],
        [['11110000', '00001111'], 8],
        [['1010101', '0101010'], 7],
        [['1001', '1001'], 0],
        [['11111111', '00000000'], 8],
        [['00110011', '11001100'], 8],
        [['01010101', '10101010'], 8],
        [['00010001', '11101110'], 6],
        [['110011', '001100'], 6],
        [['10000000', '01111111'], 8],
        [['111000', '000111'], 6],
        [['101101', '010010'], 4],
        [['111111', '111000'], 3],
        [['000000', '111111'], 6],
        [['10101010', '01010101'], 8],
        [['11011011', '00100100'], 5],
    ]

    describe('test1', () => {
        test.each(data)('%s -> %s', ([n1Str, n2Str], expected) => {
            console.log(n1Str, n2Str)
            expect(
                countDifferentBits(parseInt(n1Str, 2), parseInt(n2Str, 2))
            ).toBe(expected)
        })
    })
})
