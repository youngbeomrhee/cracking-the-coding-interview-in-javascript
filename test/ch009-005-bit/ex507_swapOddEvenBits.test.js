import { swapOddEvenBits } from '../../src/ch009-005-bit/ex507_swapOddEvenBits'

const data = [
    ['1010011010', '0101100101'],
    ['1101100110', '1110011001'],
    ['1111111111', '1111111111'],
    ['0000000000', '0000000000'],
    ['1010101010', '0101010101'],
    ['0101010101', '1010101010'],
    ['0011001100', '0011001100'],
    ['1100110011', '1100110011'],
    ['1001011001', '0110100110'],
    ['0110100110', '1001011001'],
    ['1110001110', '1101001101'],
    ['0001110001', '0010110010'],
    ['1011011011', '0111100111'],
    ['0100100100', '1000011000'],
    ['1101010101', '1110101010'],
    ['0010110101', '0001111010'],
    ['1001100110', '0110011001'],
    ['0110011001', '1001100110'],
    ['1111001100', '1111001100'],
    ['0000110011', '0000110011'],
]

describe('swapOddEvenBits', () => {
    test.each(data)('%s -> %s', (num, expected) => {
        const result = swapOddEvenBits(parseInt(num, 2))
        expect(result).toBe(parseInt(expected, 2))
    })
})
